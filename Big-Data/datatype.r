# Numeric Data Type
numeric_value <- 3.14
print(class(numeric_value)) # Output: "numeric"

# Integer Data Type
integer_value <- 10L
print(class(integer_value)) # Output: "integer"

# Logical Data Type
logical_value <- TRUE
print(class(logical_value)) # Output: "logical"

# Complex Data Type
complex_value <- 3 + 2i
print(class(complex_value)) # Output: "complex"

# Character Data Type
character_value <- "Hello, World!"
print(class(character_value)) # Output: "character"