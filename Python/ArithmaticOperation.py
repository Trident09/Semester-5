num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

# Addition
add = num1 + num2
print("Addition: ", num1, " + ", num2, " = ", add)

# Subtraction
sub = num1 - num2
print("Subtraction: ", num1, " - ", num2, " = ", sub)

# Multiplication
mul = num1 * num2
print("Multiplication: ", num1, " * ", num2, " = ", mul)

# Division
div = num1 / num2
print("Division: ", num1, " / ", num2, " = ", div)

# Floor Division
floor_div = num1 // num2
print("Floor Division: ", num1, " // ", num2, " = ", floor_div)

# Modulo (Remainder)
mod = num1 % num2
print("Modulo: ", num1, " % ", num2, " = ", mod)

# Exponentiation
exp = num1**num2
print("Exponentiation: ", num1, " ** ", num2, " = ", exp)
