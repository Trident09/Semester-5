# Using break statement
for i in range(10):
   if i == 5:
       break
   print(i)
print("Loop ended")

# Using continue statement
for i in range(10):
   if i == 5:
       continue
   print(i)
print("Loop ended")

# Using pass statement
for i in range(10):
   if i == 5:
       pass
   print(i)
print("Loop ended")
